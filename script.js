let num1 = +prompt("Введите первое число");
let num2 = +prompt("Введите второе число");
let num3 = +prompt("Введите третье число");
console.log(findMax(num1, num2, num3));

function findMax (n1, n2, n3) {
    let max = n1;
    if (n2 > max) {
        max = n2;
    } 
    if (n3 > max) {
        max = n3;
    }
    return max;
}


