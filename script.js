function sum(n1, n2) {
    return n1 + n2;
}

function difference(n1, n2) {
    if (n1 > n2) {
        return n1 - n2;
    } else if (n2 > n1) {
        return n2 - n1;
    } else return 0;
}

function multiplication(n1, n2) {
    return n1 * n2;
}

function division(n1, n2) {
    return n1 / n2;
}